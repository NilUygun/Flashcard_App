    private Boolean isQuestNumUnderLimit(){
        totalNumOfQuest = this.numOfMatching + this.numOfMChoice + this.numOfWrite;
        //System.out.println(numOfWords);
        if(totalNumOfQuest > numOfWords){
            return false;
        }else{
            return true;
        } 
    }
    
    private Boolean isQuestionNumberZero(){
        totalNumOfQuest = this.numOfMatching + this.numOfMChoice + this.numOfWrite;
        //System.out.println(numOfWords);
        if(totalNumOfQuest == 0){
            return true;
        }else{
            return false;
        } 
    }
    
