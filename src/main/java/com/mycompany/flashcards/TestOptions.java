    private void startTestButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startTestButtonActionPerformed
        // TODO add your handling code here:
        wordsList = getWordsList();
        numOfWords = getNumOfWords();
        if(!isQuestNumUnderLimit()){
            int theOption = JOptionPane.showOptionDialog(null,
            "You asked for " + totalNumOfQuest + " test questions.\nYou only have " + numOfWords + " words in your study set.", "Question Limit!",
            JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, new String[] {"Go back"}, "Go back");
            if (theOption == 0){
                JOptionPane.getRootFrame().dispose();
            }
        }else if(isQuestionNumberZero()){
            int theOption = JOptionPane.showOptionDialog(null,
            "You asked for " + totalNumOfQuest + " test questions.\nYou need to select at least 1 question to start the test.", "No Questions Selected!",
            JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, new String[] {"Go back"}, "Go back");
            if (theOption == 0){
                JOptionPane.getRootFrame().dispose();
            }
        }else{
            //ArrayList<String[][]> matchingWordsList = new ArrayList<String[][]>();

                //System.out.println("Matching words list: " + Arrays.toString(matchingWordsList));
                //System.out.printf("%d %d %d",matchingWordsList.length , matchingWordsList[0].length, matchingWordsList[0][0].length);  
                //System.out.println(matchingWordsList);
                //System.out.println(matchingWordsList[0]);
                //System.out.println(matchingWordsList[0][0]);
                //System.out.println(matchingWordsList[0][0][0]);
                this.dispose();
                //new MatchTest(matchingWordsList, wordsList, totalNumOfQuest).setVisible(true);
                if(numOfMatching != 0){
                    new MatchTest(wordsList, totalNumOfQuest, numOfMatching, numOfMChoice, numOfWrite).setVisible(true);   
                }else if(numOfMChoice != 0){
                    ArrayList<String[]> allWordsList = new ArrayList<String[]>();
                    allWordsList.addAll(wordsList);
                    new MChoiceTest(wordsList, 0, totalNumOfQuest, numOfMChoice, numOfWrite, allWordsList).setVisible(true);
                }else{
                    new WriteTest(wordsList, 0, totalNumOfQuest, numOfWrite).setVisible(true);
                }
        }
    }//GEN-LAST:event_startTestButtonActionPerformed

